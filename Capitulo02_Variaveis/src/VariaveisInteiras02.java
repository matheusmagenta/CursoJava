
public class VariaveisInteiras02 {
	public static void main(String[] args) {
		
		int n1 = 100;   //100 na base decimal
		int n2 = 0100;  //100 na base octal (inicia com 0)
		int n3 = 0x100; //100 na base hexadecimal (inicia com 0x)
		int n4 = 0b100; //100 na base binária (inicia com 0b)
		
		int quantAlunos = 020; // octal
		
		System.out.println("n1 = " + n1);
		System.out.println("n2 = " + n2);
		System.out.println("n3 = " + n3);
		System.out.println("n4 = " + n4);
		
		System.out.println("Quantos alunos?" + quantAlunos);
	}

}
